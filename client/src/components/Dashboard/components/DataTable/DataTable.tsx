                    <Input
                        placeholder="Filter by merchant..."
                        value={(table.getColumn("merchant")?.getFilterValue() as string) ?? ""}
                        onChange={(event) =>
                            table.getColumn("merchant")?.setFilterValue(event.target.value)
                        }
                        className="max-w-sm"
                    />
